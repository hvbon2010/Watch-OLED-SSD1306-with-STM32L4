/* StartDefaultTask function */
void UnitSecondScrollTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
		if(sec++ == 59)
		{
			sec = 0;
			if(min++ == 59)
			{
				min = 0;
				if(hour++ == 23)
				{
					hour = 0;
				}
				m_ssd1306.param.scroll_num[SCROLL_HOUR_UNIT].num = hour%10;
				m_ssd1306.param.scroll_num[SCROLL_HOUR_UNIT].scroll = SCROLL;
				if(hour%10 == 0)
				{
					m_ssd1306.param.scroll_num[SCROLL_HOUR_DEC].num = hour/10;
					m_ssd1306.param.scroll_num[SCROLL_HOUR_DEC].scroll = SCROLL;		
				}					
			}
			m_ssd1306.param.scroll_num[SCROLL_MIN_UNIT].num = min%10;
			m_ssd1306.param.scroll_num[SCROLL_MIN_UNIT].scroll = SCROLL;
			if(min%10 == 0)
			{
				m_ssd1306.param.scroll_num[SCROLL_MIN_DEC].num = min/10;
				m_ssd1306.param.scroll_num[SCROLL_MIN_DEC].scroll = SCROLL;
			}
		}
		m_ssd1306.param.scroll_num[SCROLL_SEC_UNIT].num = sec%10;
		m_ssd1306.param.scroll_num[SCROLL_SEC_UNIT].scroll = SCROLL;
		if(sec%10 == 0)
		{
			m_ssd1306.param.scroll_num[SCROLL_SEC_DEC].num = sec/10;
			m_ssd1306.param.scroll_num[SCROLL_SEC_DEC].scroll = SCROLL;
		}
		
		m_ssd1306.api.scroll_number_all(&m_ssd1306);
		osDelay(500);
  }
  /* USER CODE END 5 */ 
}

